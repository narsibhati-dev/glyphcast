import fs from "node:fs";
import path from "node:path";

const root = path.join(import.meta.dirname, "..");
const componentsDir = path.join(root, "src/components/ascii-animations");
const indexPath = path.join(componentsDir, "index.ts");

function toPascalCase(value) {
  return value
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((segment) => segment[0].toUpperCase() + segment.slice(1))
    .join("");
}

function toTitleCase(value) {
  return value
    .split(/[-_]+/)
    .filter(Boolean)
    .map((segment) => segment[0].toUpperCase() + segment.slice(1))
    .join(" ");
}

function toUpperSnakeCase(value) {
  return value.replace(/[^a-zA-Z0-9]+/g, "_").toUpperCase();
}

function getComponentName(filePath, fallbackId) {
  const source = fs.readFileSync(filePath, "utf8");
  const match = source.match(/export\s+default\s+function\s+([A-Za-z0-9_]+)/);
  return match?.[1] ?? toPascalCase(fallbackId);
}

const componentFiles = fs
  .readdirSync(componentsDir)
  .filter((name) => name.endsWith(".tsx") && name !== "index.ts")
  .sort((a, b) => a.localeCompare(b));

if (componentFiles.length === 0) {
  throw new Error("No ASCII component .tsx files found.");
}

const entries = componentFiles.map((fileName) => {
  const id = fileName.replace(/\.tsx$/, "");
  const pascalId = toPascalCase(id);
  const componentPath = path.join(componentsDir, fileName);
  const componentName = getComponentName(componentPath, id);
  const upperSnake = toUpperSnakeCase(id);

  return {
    id,
    fileName,
    pascalId,
    componentName,
    title: toTitleCase(id),
    description: `ASCII ${id} animation.`,
    upperSnake,
  };
});

const importBlocks = entries
  .map(
    ({ id, pascalId }) => `import {
  APPEARANCE as ${pascalId}Appearance,
  CHARS as ${pascalId}Chars,
  FPS as ${pascalId}Fps,
  FRAMES as ${pascalId}Frames,
} from "./${id}";`,
  )
  .join("\n\n");

const showcaseEntries = entries
  .map(
    ({ id, title, description, fileName, pascalId, componentName }) => `  {
    id: "${id}",
    title: "${title}",
    description: "${description}",
    filename: "${fileName}",
    accentColor: "#B54B00",
    componentName: "${componentName}",
    frames: ${pascalId}Frames,
    fps: ${pascalId}Fps,
    chars: ${pascalId}Chars,
    appearance: ${pascalId}Appearance as ASCIIAppearance,
  },`,
  )
  .join("\n");

const exportBlocks = entries
  .map(
    ({ id, upperSnake }) => `export {
  APPEARANCE as ${upperSnake}_APPEARANCE,
  CHARS as ${upperSnake}_CHARS,
  FPS as ${upperSnake}_FPS,
  FRAMES as ${upperSnake}_FRAMES,
} from "./${id}";`,
  )
  .join("\n\n");

const source = `import type { ASCIIAppearance } from "@/lib/ascii-config";
${importBlocks}

export interface ASCIIShowcaseEntry {
  id: string;
  title: string;
  description: string;
  filename: string;
  accentColor: string;
  componentName: string;
  frames: string[];
  fps: number;
  chars: string;
  appearance: ASCIIAppearance;
}

// This file is auto-generated by scripts/generate-ascii-index.mjs.
export const ASCII_SHOWCASE: ASCIIShowcaseEntry[] = [
${showcaseEntries}
];

${exportBlocks}
`;

fs.writeFileSync(indexPath, source);
console.log(
  `Generated ${path.relative(root, indexPath)} from ${entries.length} components.`,
);
